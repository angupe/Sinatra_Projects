class Calculator

    # attr_accessor :number_1, :number_2

    def self.add(number_1,number_2)
        number_1 + number_2
    end

    def self.sub(number_1,number_2)
        number_1 - number_2
    end

    def self.mul(number_1,number_2)
        number_1 * number_2
    end

    def self.div(number_1,number_2)
        number_1 / number_2
    end

end
